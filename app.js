$(window).on("keydown", function (event) {
  
  if (event.keyCode === 37) {
    snake.nextDirection = [0, -1];
  }

  if (event.keyCode === 39) {
    snake.nextDirection = [0, 1];
  }

  if (event.keyCode === 38) {
    snake.nextDirection = [-1, 0];
  }

  if (event.keyCode === 40) {
    snake.nextDirection = [1, 0];
  }
});

